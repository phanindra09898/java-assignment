 public class SimpleInterestCompoundInterest
 {  
   public static void main (String args[])  
    {   float p, r, t, si, co;
              p = 13000;  r = 12; t = 2;  
               si  = (p*r*t)/100;   
              System.out.println("Simple Interest is: " +si);  
              co = (p*r*t)/100;
              System.out.println("Compound Interest is: " +co);
    }
     
 }  
